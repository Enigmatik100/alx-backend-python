#!/usr/bin/env python3
"""concat two string"""


def concat(str1: str, str2: str) -> str:
    """
    Return concatenation of str1 and str2
    :param str1: str
        first string
    :param str2: str
        second string
    :return: string
        concatenation of str1 and str2
    """

    return str1 + str2
